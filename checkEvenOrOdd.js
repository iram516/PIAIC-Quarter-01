"use strict";
function checkEvenOrOdd(num) {
    if (num % 2 === 0) {
        return "Even";
    }
    else {
        return "Odd";
    }
}
;
console.log(checkEvenOrOdd(47));
console.log(checkEvenOrOdd(42));
console.log(checkEvenOrOdd(39));
console.log(checkEvenOrOdd(50));
console.log(checkEvenOrOdd(331));
